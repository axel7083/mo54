const selectRandomVehicles = () => {
    const copy = [...vehiclesIds];
    const count = getRandomInt(copy.length - 1);
    const output: string[] = [];

    for(let i = 0; i < count; i++) {
        output.push(copy.splice(getRandomInt(copy.length - 1), 1)[0]);
    }

    return output;
}

